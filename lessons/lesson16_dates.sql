----TIME ,DATE ,TIMESTAMP FORMATTING

SELECT CURRENT_DATE AS c_date,
       CURRENT_TIMESTAMP as c_timesatamp;


SHOW TIME ZONE;

select * from pg_timezone_names;

SET TIME ZONE 'Asia/Istanbul';

SHOW DATESTYLE;
--ISO, MDY

SET DATASTYLE 'ISO, YMD';

SHOW LC_COLLATE 
SET LC_COLLATE  'tr_TR.UTF-8'

select 
CURRENT_DATE,
TO_DATE('2020-01-10','YYY-MM-DD') as to_date,
DATE_PART('YEAR','2020-01-10'::DATE) as year_value,
DATE_PART('DAY','2020-01-10'::DATE) as day_value,
DATE_PART('MONTH','2020-01-10'::DATE) as month_value,
DATE_PART('MONTH','2020-01-10'::TIMESTAMP) as month_value,
CURRENT_DATE-TO_DATE('2023-12-25','YYY-MM-DD') as number_of_days,
TO_TIMESTAMP('2024-01-25 10:10:59','YYYY-MM-DD HH:MI:SS'),
TO_CHAR('2020-01-10'::DATE,'YYYY-MM-DD'),
TO_CHAR('2020-01-10 10:20:34'::TIMESTAMP,'YYYY MONTH DAY')



